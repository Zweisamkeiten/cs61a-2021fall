def autocorrect(typed_word, valid_words, diff_function, limit):
    """Returns the element of VALID_WORDS that has the smallest difference
    from TYPED_WORD. Instead returns TYPED_WORD if that difference is greater
    than LIMIT.

    Arguments:
        typed_word: a string representing a word that may contain typos
        valid_words: a list of strings representing valid words
        diff_function: a function quantifying the difference between two words
        limit: a number

    >>> ten_diff = lambda w1, w2, limit: 10 # Always returns 10
    >>> autocorrect("hwllo", ["butter", "hello", "potato"], ten_diff, 20)
    'butter'
    >>> first_diff = lambda w1, w2, limit: (1 if w1[0] != w2[0] else 0) # Checks for matching first char
    >>> autocorrect("tosting", ["testing", "asking", "fasting"], first_diff, 10)
    'testing'
    """
    # BEGIN PROBLEM 5
    # 如果typed_word包含在valid_words, 返回typed_word
    if typed_word in valid_words:
        return typed_word
    # 否则 返回valid_words中与typed_word根据差异函数得出差异最小的word
    # 如果得出最小差异单词差异仍然大于limit则返回typed_word
    else:
        lowest_diff_word = min(
            valid_words,
            key=lambda valid_word: diff_function(typed_word, valid_word, limit),
        )
        return (
            typed_word
            if diff_function(typed_word, lowest_diff_word, limit) > limit
            else lowest_diff_word
        )
    # END PROBLEM 5


def feline_flips(start, goal, limit):
    """A diff function for autocorrect that determines how many letters
    in START need to be substituted to create GOAL, then adds the difference in
    their lengths and returns the result.

    Arguments:
        start: a starting word
        goal: a string representing a desired goal word
        limit: a number representing an upper bound on the number of chars that must change

    >>> big_limit = 10
    >>> feline_flips("nice", "rice", big_limit)    # Substitute: n -> r
    1
    >>> feline_flips("range", "rungs", big_limit)  # Substitute: a -> u, e -> s
    2
    >>> feline_flips("pill", "pillage", big_limit) # Don't substitute anything, length difference of 3.
    3
    >>> feline_flips("roses", "arose", big_limit)  # Substitute: r -> a, o -> r, s -> o, e -> s, s -> e
    5
    >>> feline_flips("rose", "hello", big_limit)   # Substitute: r->h, o->e, s->l, e->l, length difference of 1.
    5
    """
    # BEGIN PROBLEM 6
    def count(cstart, cgoal, count_change):
        if count_change > limit:
            return limit + 1
        elif cstart == "":
            return count_change + len(cgoal)
        elif cgoal == "":
            return count_change + len(cstart)
        elif cstart[0] == cgoal[0]:
            return count(cstart[1:], cgoal[1:], count_change)
        else:
            return count(cstart[1:], cgoal[1:], count_change + 1)

    return count(start, goal, 0)
    # END PROBLEM 6


